'use client';

import { useState } from 'react';
import Link from 'next/link';
import { supabase } from '@/lib/supabaseClient';
import { HabitifyLogo } from '@/components/ui/HabitifyLogo';

export default function LoginPage() {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState('');

  const handleLogin = async (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);
    setError('');
    try {
      const { data, error: err } = await supabase.auth.signInWithPassword({ email, password });
      if (err) throw err;
      if (data.user) {
        window.location.href = '/dashboard';
        return;
      }
    } catch (err: unknown) {
      setError(err instanceof Error ? err.message : 'Failed to sign in');
    } finally {
      setLoading(false);
    }
  };

  const handleGoogleLogin = async () => {
    setError('');
    try {
      const { error: err } = await supabase.auth.signInWithOAuth({ provider: 'google' });
      if (err) throw err;
    } catch (err: unknown) {
      setError(err instanceof Error ? err.message : 'Failed to sign in with Google');
    }
  };

  const handleAppleLogin = async () => {
    setError('');
    try {
      const { error: err } = await supabase.auth.signInWithOAuth({ provider: 'apple' });
      if (err) throw err;
    } catch (err: unknown) {
      setError(err instanceof Error ? err.message : 'Failed to sign in with Apple');
    }
  };

  return (
    <div className="w-full max-w-[400px] flex flex-col items-center">
      <HabitifyLogo className="mb-6" />
      <h1 className="text-xl font-semibold text-[#111827]">Welcome to Habitify</h1>
      <p className="mt-1 text-sm text-[#6B7280]">Your journey to better habits starts here</p>

      <div className="mt-8 w-full space-y-3">
        <button
          type="button"
          onClick={handleGoogleLogin}
          className="flex w-full items-center justify-center gap-3 rounded-lg border border-[#E5E7EB] bg-white px-4 py-3 text-sm font-medium text-[#111827] hover:bg-[#F9FAFB]"
        >
          <svg className="h-5 w-5" viewBox="0 0 24 24" aria-hidden>
            <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
            <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
            <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
            <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
          </svg>
          Sign In with Google
        </button>
        <button
          type="button"
          onClick={handleAppleLogin}
          className="flex w-full items-center justify-center gap-3 rounded-lg bg-[#111827] px-4 py-3 text-sm font-medium text-white hover:bg-[#374151]"
        >
          <svg className="h-5 w-5" fill="currentColor" viewBox="0 0 24 24" aria-hidden>
            <path d="M17.05 20.28c-.98.95-2.05.88-3.08.4-1.09-.5-2.08-.48-3.24 0-1.44.62-2.2.44-3.06-.4C2.79 15.25 3.51 7.59 9.05 7.31c1.35.07 2.29.74 3.08.8 1.18-.24 2.31-.93 3.57-.84 1.51.12 2.65.72 3.4 1.8-3.12 1.87-2.38 5.98.48 7.13-.57 1.5-1.31 2.99-2.54 4.09l.01-.01zM12.03 7.25c-.15-2.23 1.66-4.07 3.74-4.25.29 2.58-2.34 4.5-3.74 4.25z" />
          </svg>
          Sign In with Apple
        </button>
      </div>

      <div className="my-6 flex w-full items-center gap-3">
        <div className="h-px flex-1 bg-[#E5E7EB]" />
        <span className="text-xs text-[#9CA3AF]">or continue with email</span>
        <div className="h-px flex-1 bg-[#E5E7EB]" />
      </div>

      {error && (
        <div className="mb-4 w-full rounded-lg border border-red-200 bg-red-50 px-3 py-2 text-sm text-red-700">
          {error}
        </div>
      )}

      <form onSubmit={handleLogin} className="w-full space-y-4">
        <div>
          <label htmlFor="login-email" className="block text-xs font-medium uppercase tracking-wide text-[#374151]">
            Email <span className="text-red-500">*</span>
          </label>
          <input
            id="login-email"
            type="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            required
            placeholder="your-email@example.com"
            className="mt-1.5 w-full rounded-lg border border-[#E5E7EB] bg-white px-3 py-2.5 text-sm text-[#111827] placeholder:text-[#9CA3AF] focus:border-[#2a67f4] focus:outline-none focus:ring-1 focus:ring-[#2a67f4]"
          />
        </div>
        <div>
          <label htmlFor="login-password" className="block text-xs font-medium uppercase tracking-wide text-[#374151]">
            Password <span className="text-red-500">*</span>
          </label>
          <input
            id="login-password"
            type="password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            required
            placeholder="Password"
            className="mt-1.5 w-full rounded-lg border border-[#E5E7EB] bg-white px-3 py-2.5 text-sm text-[#111827] placeholder:text-[#9CA3AF] focus:border-[#2a67f4] focus:outline-none focus:ring-1 focus:ring-[#2a67f4]"
          />
        </div>
        <button
          type="submit"
          disabled={loading}
          className="w-full rounded-lg bg-[#2a67f4] py-3 text-sm font-medium text-white hover:bg-[#2360dd] disabled:opacity-60"
        >
          {loading ? 'Signing in...' : 'Sign In'}
        </button>
      </form>

      <p className="mt-6 text-center text-sm text-[#6B7280]">
        No account?{' '}
        <Link href="/signup" className="font-medium text-[#2a67f4] hover:underline">
          Create an account
        </Link>
      </p>
      <p className="mt-1 text-center text-sm text-[#6B7280]">
        Forgot Password?{' '}
        <Link href="/forgot-password" className="font-medium text-[#2a67f4] hover:underline">
          Reset here
        </Link>
      </p>

      <p className="mt-10 text-center text-xs text-[#9CA3AF]">
        By continuing, you agree to our{' '}
        <Link href="/" className="text-[#2a67f4] hover:underline">Terms of Service</Link>
        {' '}and{' '}
        <Link href="/" className="text-[#2a67f4] hover:underline">Privacy Policy</Link>
      </p>
    </div>
  );
}
